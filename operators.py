print ('Sum: ', a + b)  

# # subtraction
print ('Subtraction: ', a - b)   

# # multiplication
print ('Multiplication: ', a * b)  

# # division
print ('Division: ', a / b) 

# # floor division
print ('Floor Division: ', a // b)

# # modulo
print ('Modulo: ', a % b)  

# # a to the power b
print ('Power: ', a ** b)


